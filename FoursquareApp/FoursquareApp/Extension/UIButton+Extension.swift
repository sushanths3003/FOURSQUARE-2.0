import Foundation
import UIKit
extension UIButton {
    
    static func ImageEdgeInsetsForSidemenuButton(button: UIButton) {
        button.imageEdgeInsets = UIEdgeInsets(top: 31, left: 2, bottom: 31, right: 35)
    }
}
